package com.mmm.findtherythm3.conf;

public class Instrument {
	
	private int sonId;
	private int imageId;
	
	public Instrument() {
		super();
	}
	public int getSonId() {
		return sonId;
	}
	public void setSonId(int sonId) {
		this.sonId = sonId;
	}
	public int getImageId() {
		return imageId;
	}
	public void setImageId(int imageId) {
		this.imageId = imageId;
	}
	
}
